function [x,y,z,xtx,xty,xtz,xd,yd,zd,xtxd,xtyd,xtzd]=quan(h)
x=[];
y=[];
z=[];
xtx=[];
xty=[];
xtz=[];
xd=[];
yd=[];
zd=[];
xtxd=[];
xtyd=[];
xtzd=[];
h0 = 350e-3;
L0 = 60e-3;
dt=0.0001;
for i=1:length(h)
    h1=h(i,1);
    h2=h(i,2);
    h3=h(i,3);
    h1d=h(i,4);
    h2d=h(i,5);
    h3d=h(i,6);
    h1t=h1+h1d*dt;
    h2t=h2+h2d*dt;
    h3t=h3+h3d*dt;
    Aq=jieA(h1,h2,h3,h0,L0);
    At=jieA(h1t,h2t,h3t,h0,L0);
    o=Aq*[0;0;0;1];
    x=[x;o(1)];
    y=[y;o(2)];
    z=[z,o(3)];
    ot=At*[0;0;0;1];
    xd=[xd;(ot(1)-x)/dt];
    yd=[yd;(ot(2)-y)/dt];
    zd=[zd;(ot(3)-y)/dt];
    A=Aq(1:3,1:3);
    Az=A';
    la=Az*[1;0;0];
    lb=Az*[sqrt(3)*L0/2;3*L0/2;h2-h1];
    lr=[0;0;1];
    la=la./norm(la);
    lb=lb/norm(lb);
    %求解三个旋转轴的坐标
    a2xt=[la,lb,lr];
    %角度坐标转换矩阵
    [a,b,r]=jieabr(h1,h2,h3,L0);
    [at,bt,rt]=jieabr(h1t,h2t,h3t,L0);
    xt=a2xt*[a;b;r];
    xtt=a2xt*[at;bt;rt];
    xtx=[xtx;xt(1)];
    xty=[xty;xt(2)];
    xtz=[xtz;xt(3)];
    xtxd=[xtxd;(xtt(1)-xtx)/dt];
    xtyd=[xtyd;(xtt(2)-xty)/dt];
    xtzd=[xtzd;(xtt(3)-xtz)/dt];
end
end